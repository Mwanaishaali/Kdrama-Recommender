function showRecommendations() {
    const genre = document.getElementById("genre").value;
    const recommendationsDiv = document.getElementById("recommendations");
    recommendationsDiv.innerHTML = "";

    if (genre && recommendations[genre]) {
        recommendations[genre].slice(0, 4).forEach(drama => {
            const dramaDiv = document.createElement("div");
            dramaDiv.classList.add("top");

            dramaDiv.innerHTML = `
                <div class="image1">
                    <img src="${drama.image}" alt="${drama.title}">
                </div>
                <div class="text">
                    <h4>${drama.title}</h4>
                    <p>${drama.description}</p>
                    <p><strong>Review:</strong> ${drama.review}</p>
                </div>
            `;
            recommendationsDiv.appendChild(dramaDiv);
        });
    }
}
